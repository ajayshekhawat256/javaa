public class Switchcase {
    public static void main(String args[]){
        int numbers=2;
        switch(numbers){
            case 1:System.out.println("MANGO");
            break;
            case 2:System.out.println("PAPAYA");
            break;
            case 3:System.out.println("PINE APPLE");
            break;
            default:System.out.println("THIS IS A DREAM");
        }
    }
}
